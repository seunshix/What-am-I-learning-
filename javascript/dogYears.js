// my age
let myAge = 25;

// first two years of a dog's life counts as 10.5 dog years each
let earlyYears = 2;
earlyYears *= 10.5;

// subtracting 2 from myAge
let laterYears = myAge - 2;

// each following year equates to 4 dog years
laterYears *= 4;

console.log(`earlyYears ${earlyYears}`);
console.log(`laterYears ${laterYears}`);

myAgeInDogYears = earlyYears + laterYears;

myName = "Eniola Olamide".toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} which is ${myAgeInDogYears} in dog years`);
